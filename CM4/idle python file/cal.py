def addition(x,y):
    sum = x+y
    print(sum)
def subtract(x,y):
    sub = x-y
    print(sub)
def mult(x,y):
    mul = x*y
    print(mul)
def devi(x,y):
    dv = x/y
    print(dv)
def modu(x,y):
    mo = x%y
    print(mo)
def new(n):
    for i in range(n):
        print(i+1)
def newone(n):
    for i in range(n):
        print(i+1)
